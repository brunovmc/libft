#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int x;

	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			write(fd, "-", 1);
			n = n * -1;
		}
		if (n / 10 > 0)
			ft_putnbr_fd(n / 10, fd);
		x = n % 10 + 48;
		write(fd, &x, 1);
	}
}
